     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $article = new Todo;
      $article->taskName = $request->taskName;
      $article->taskContents = $request->taskContents;
      $article->isDone = false;
      $article->save();
      return redirect('api/alltasks');
    }

